class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int a[], int n){
        
       long long int maxx=a[0],sum=0,i;
       for(i=0;i<n;i++)
       {    
           long long aa=a[i];
           sum=max(sum+a[i],aa);
           maxx=max(sum,maxx);
       }
       return maxx;
        
    }
};

